/* =============================================================
 * PLANETARY POSITIONS (VSOP87D truncated series)
 * Coefficients from Bretagnon & Francou 1988, published in
 * Meeus "Astronomical Algorithms" Appendix III.
 * Units: A in 1e-8 rad, B in rad, C in rad/millennia.
 * Final longitude/latitude = sum(A*cos(B+C*t)) * 1e-8 rad,
 * where t = (JDE - 2451545) / 365250.
 * Accuracy: within a few arcminutes over 369 CE – 3369 CE (3000-yr window).
 * ============================================================= */

// Evaluate one VSOP87 variable (L, B, or R) given series-of-series
function evalVsop(series, t) {
  let total = 0;
  for (let s = series.length - 1; s >= 0; s--) {
    let sum = 0;
    const terms = series[s];
    for (let i = 0; i < terms.length; i++) {
      const [A, B, C] = terms[i];
      sum += A * Math.cos(B + C * t);
    }
    total = total * t + sum;
  }
  return total * 1e-8;
}

