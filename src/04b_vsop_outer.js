// Main planet ephemeris: returns {lon, lat, r} geocentric ecliptic of date (apparent, with nutation)
// planetId: 'mercury','venus','mars','jupiter','saturn','uranus','neptune','sun'
const PLANETS_VSOP = {
  mercury: [VSOP_MER_L, VSOP_MER_B, VSOP_MER_R],
  venus:   [VSOP_VEN_L, VSOP_VEN_B, VSOP_VEN_R],
  earth:   [VSOP_EAR_L, VSOP_EAR_B, VSOP_EAR_R],
  mars:    [VSOP_MAR_L, VSOP_MAR_B, VSOP_MAR_R],
  jupiter: [VSOP_JUP_L, VSOP_JUP_B, VSOP_JUP_R],
  saturn:  [VSOP_SAT_L, VSOP_SAT_B, VSOP_SAT_R],
  uranus:  [VSOP_URA_L, VSOP_URA_B, VSOP_URA_R],
  neptune: [VSOP_NEP_L, VSOP_NEP_B, VSOP_NEP_R]
};

// Heliocentric ecliptic coordinates of planet at JDE
function planetHeliocentric(planet, jde) {
  const t = (jde - J2000) / 365250; // Julian millennia
  const [Lseries, Bseries, Rseries] = PLANETS_VSOP[planet];
  const L = mod2pi(evalVsop(Lseries, t));
  const B = evalVsop(Bseries, t);
  const R = evalVsop(Rseries, t);
  return { L, B, R };
}

// Heliocentric ecliptic -> rectangular (X, Y, Z)
function eclipticToRectangular(L, B, R) {
  const cosB = Math.cos(B);
  return {
    x: R * cosB * Math.cos(L),
    y: R * cosB * Math.sin(L),
    z: R * Math.sin(B)
  };
}

// Sun apparent geocentric ecliptic longitude & latitude (of date).
// Derived from Earth VSOP87: Sun = -Earth heliocentric.
// Adds nutation and aberration.
function sunPosition(jde) {
  const t = (jde - J2000) / 365250;
  const Le = mod2pi(evalVsop(VSOP_EAR_L, t));
  const Be = evalVsop(VSOP_EAR_B, t);
  const Re = evalVsop(VSOP_EAR_R, t);
  // Sun longitude = Earth's heliocentric longitude + pi
  let sunLon = Le + Math.PI;
  let sunLat = -Be;
  // Nutation in longitude (apparent)
  const T = (jde - J2000) / 36525;
  const { dPsi } = nutation(T);
  sunLon += dPsi;
  // Aberration (approx -20.4898"/R)
  sunLon -= (20.4898 / 3600 * DEG) / Re;
  return { lon: mod2pi(sunLon), lat: sunLat, r: Re };
}

// Geocentric apparent ecliptic longitude/latitude/distance of a planet
// (with light-time correction & aberration).
function planetGeocentric(planet, jde) {
  if (planet === 'sun') return sunPosition(jde);
  const T = (jde - J2000) / 36525;
  const { dPsi } = nutation(T);
  // Earth position
  const earth = planetHeliocentric('earth', jde);
  const Ex = earth.R * Math.cos(earth.B) * Math.cos(earth.L);
  const Ey = earth.R * Math.cos(earth.B) * Math.sin(earth.L);
  const Ez = earth.R * Math.sin(earth.B);
  // First pass planet position
  let p = planetHeliocentric(planet, jde);
  let px = p.R * Math.cos(p.B) * Math.cos(p.L);
  let py = p.R * Math.cos(p.B) * Math.sin(p.L);
  let pz = p.R * Math.sin(p.B);
  let dx = px - Ex, dy = py - Ey, dz = pz - Ez;
  let delta = Math.sqrt(dx*dx + dy*dy + dz*dz);
  // Light-time iteration
  for (let iter = 0; iter < 2; iter++) {
    const tau = 0.0057755183 * delta; // days per AU
    p = planetHeliocentric(planet, jde - tau);
    px = p.R * Math.cos(p.B) * Math.cos(p.L);
    py = p.R * Math.cos(p.B) * Math.sin(p.L);
    pz = p.R * Math.sin(p.B);
    dx = px - Ex; dy = py - Ey; dz = pz - Ez;
    delta = Math.sqrt(dx*dx + dy*dy + dz*dz);
  }
  let lon = Math.atan2(dy, dx);
  const lat = Math.atan2(dz, Math.sqrt(dx*dx + dy*dy));
  // Apparent: add nutation in longitude
  lon = mod2pi(lon + dPsi);
  return { lon, lat, r: delta };
}

// Is a planet retrograde? (daily longitude change negative)
function isRetrograde(planet, jde) {
  if (planet === 'sun' || planet === 'moon') return false;
  const p1 = planetGeocentric(planet, jde - 1);
  const p2 = planetGeocentric(planet, jde + 1);
  let d = p2.lon - p1.lon;
  if (d > Math.PI) d -= TWO_PI;
  if (d < -Math.PI) d += TWO_PI;
  return d < 0;
}
