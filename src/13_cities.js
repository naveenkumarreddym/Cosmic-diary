function searchCities(query, limit = 10) {
  if (!query) return [];
  const q = query.toLowerCase().trim();
  if (q.length < 1) return [];
  const results = [];
  for (const c of CITIES) {
    const name = c[0].toLowerCase();
    if (name.startsWith(q)) results.push({ c, score: 2 });
    else if (name.includes(q)) results.push({ c, score: 1 });
    if (results.length > limit * 4) break;
  }
  results.sort((a, b) => b.score - a.score);
  return results.slice(0, limit).map(r => ({
    name: r.c[0], country: r.c[1], lat: r.c[2], lon: r.c[3], tz: r.c[4]
  }));
}
